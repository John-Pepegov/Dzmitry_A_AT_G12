package project.pretask;

public class IncrementDecrementExample {
    //класс IncrementDecrementExample, объявите целочисленную переменную и используйте операторы инкремента и декремента для изменения её значения;
    //выведите результаты на экран;
    public static void main(String[] args) {
        int a = 1;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
    }
}
